# Creating lists in Python

# Creating a list of strings
strList = ["Asia","Africa","America"]
print(strList)

# Creating a list of integers
intList = [1,2,3,4]
print(intList)

# Creating a list of floating point numbers
floatList = [2.1, 3.4, 4.5]
print(floatList)

# Creating a list of values
anyList = [1, "Name", 24, "Designation", 55.4]
print(anyList)

print(type(strList))
print(type(anyList))

# Using list() to create a list
newList = list((1,2,"free",4.5,"man"))
print(newList)





